package Condition;

public class Nested_If3_cond {
	public static void main(String args[]) {
		int a = 40;
		int b = 50;
		if(a==40) {
			if(b==50) {
				System.out.println("a value is 40 & b value is 50");
			}
			else {
				System.out.println("else stmt of second cond");
			}
		}
		else {
			System.out.println("else stmt of first cond");
		}
	}
	}


